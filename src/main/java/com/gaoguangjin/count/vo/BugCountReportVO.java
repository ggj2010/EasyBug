package com.gaoguangjin.count.vo;

public class BugCountReportVO {
	private String levelStr;
	private String statusStr;
	private String allBugStr;
	private String newBugStr;
	private String moduleStr;
	private String userStr;
	
	public String getLevelStr() {
		return levelStr;
	}
	
	public void setLevelStr(String levelStr) {
		this.levelStr = levelStr;
	}
	
	public String getStatusStr() {
		return statusStr;
	}
	
	public void setStatusStr(String statusStr) {
		this.statusStr = statusStr;
	}
	
	public String getAllBugStr() {
		return allBugStr;
	}
	
	public void setAllBugStr(String allBugStr) {
		this.allBugStr = allBugStr;
	}
	
	public String getNewBugStr() {
		return newBugStr;
	}
	
	public void setNewBugStr(String newBugStr) {
		this.newBugStr = newBugStr;
	}
	
	public String getModuleStr() {
		return moduleStr;
	}
	
	public void setModuleStr(String moduleStr) {
		this.moduleStr = moduleStr;
	}
	
	public String getUserStr() {
		return userStr;
	}
	
	public void setUserStr(String userStr) {
		this.userStr = userStr;
	}
	
}
